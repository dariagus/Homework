t = int(input())
m = int(input())

while t > 0 or m > 0:
    t -= 1
    m -= 0.5
    if t > 0 and m == 0:
        print("Моющее средство закончилось.Осталось", t,"тарелок")
    elif t == 0 and m > 0:
        print("Все тарелки вымыты. Осталось", m,"ед. моющего средства")
    elif t == 0 and m == 0:
        print("Все тарелки вымыты, моющее средство закончилось")
